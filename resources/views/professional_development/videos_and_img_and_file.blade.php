                    <h2>1. التعليم البصري: تأثير المرئيات على الحِمل المعرفي</h2>
                    <p>لجأت العديد من الدول إلى تضمين موارد التعلم الخاصة بطلاب الجامعات عبر شبكة الإنترنت، في محاولة جادة لاحتواء الانتشار المفزع لفيروس كورونا (كوفيد-19)، خصوصًا بين الفئة التي تعد اختيارًا مثاليًا للفيروس، لكي ينتشر إلى أكبر قدر ممكن من الأسر.</p>
                    <p>وفي حين
                        أن وسائل التعليم الإلكترونية تعد متاحة بسهولة للجميع، إلا أنه لا يزال هناك
                        العديد من القضايا التربوية التي تحتاج إلى إعادة النظر، خاصة عندما يتعلق الأمر باختيار
                        الطريقة الأنسب من بينهم.</p>
                    <p>نستعرض
                        في هذا المقال أهم النقاط التي من خلالها يمكن تحويل التعليم الإلكتروني إلى وسيلة
                        تعليم ناجحة ومُرضية.</p>
                    <h2>1. دروس تفاعلية: الحفاظ على تركيز وتفاعل المتلقي</h2> <p>عوضًا عن استخدام البث الحي المشتت للذهن والقاتل للعمليات التفاعلية بين المعلم والطالب، قد يفكر العديد من أعضاء هيئة التدريس في استخدام مكالمات الفيديو لإحياء المحادثات، ويُعدّ ذلك من الطرق الفعالة للغاية، ولكن للمجموعات التي لا تتجاوز العشرة طلّاب.</p>
                    <p>يمكن لبناء شبكة من المجموعات الصغيرة المترابطة ببعضها البعض أن يكون حلاً ممتازًا، لاحتوائه أكبر عدد من الطلاب بجانب الحفاظ على تفاعلهم وتركيزهم في كل مجموعة.</p>
                    <p>ويعد
                        تطبيق Zoom أحد أهم التطبيقات التي تدعم هذه العملية، حيثُ يسمح التطبيق بإجراء
                        المكالمة بالفيديو في مجموعات بأريحية كبيرة، كما يمكن للمعلمين الرد على أسئلة
                        الطلاب في حال تراكمها من خلال نافذة الدردشة.</p>
                    <p>ينخرط
                        طلاب المرحلة الجامعية بشكل أفضل في المحاضرات التفاعلية، التي تعمل على شد انتباه
                        الطالب والحفاظ على تركيزه، وعلى نقيض ذلك في المحاضرات التي تمتد لساعة أو أكثر
                        لمجرد معلّم يتحدث، فزيادة الفترة الزمنية التي يتلقى بها الطلاب المعلومات في المحاضرة
                        يعمل على دفع الملل إلى نفوسهم.</p>
                    <p>وهنا نوصي
                        بتقصير المدة الزمنية للمحاضرات عبر الإنترنت إلى 10-15 دقيقة، تتخللها فترات
                        وجيزة من الأسئلة والأجوبة؛ ومقاطع فيديو تعليمية قصيرة ذات صلة بموضوع اليوم؛
                        ومهام استقصائية مدتها 10 دقائق مثل الكشف عن آخر التطورات البحثية مثلاً، ويتبع
                        ذلك عرض الطلاب نتائج بحوثهم على المعلّم وإجراء مناقشة جماعية بينهم.</p>
                    <h2>2. التعليم البصري: تأثير المرئيات على الحِمل المعرفي</h2> <p>كيف يمكن للعناصر المرئية أن يكون لها تأثير كبير على تجربة التعليم الإلكتروني؟ هناك تفسير علمي كامل وراء ذلك، وتعدّ <a href="https://en.wikipedia.org/wiki/Cognitive_load">نظرية الحِمل المعرفي</a> التي وضعها «جون سويلر» من أكثر النظريات إقناعًا.</p>
                    <p>تفترض
                        النظرية التي وضعها سويلر أن العقل يملك ذاكرة قصيرة المدى ومحدودة السعة؛ لا تتمكن
                        إلا من استقبال ومعالجة عناصر محدودة من المعلومات (ذاكرة مؤقتة)، وهناك ذاكرة
                        أخرى طويلة المدى وذات سعة غير محدودة يتم تخزين المعلومات فيها بعد معالجتها (ذاكرة
                        دائمة)، وأنّ الذاكرة المؤقتة تشارك في فهم المعلومات وترميزها لدى الذاكرة الدائمة،
                        فإذا تراكمت المعلومات التي تتلقاها الذاكرة المؤقتة، أدى ذلك إلى حمل ذهني زائد
                        عليها، وبالتالي تفشل عملية التعلم.</p>
                    <p>وفقًا <a>للدراسات التي أُجريت في </a>
                        <a href="https://brainrules.blogspot.com/2009/12/worth-thousand-words.html">علم الأعصاب</a>، والتي أشار إليها «جون مدينا» في كتابه «قواعد الدماغ»؛ تذكّر المشاركون في الدراسات حوالي 10% فقط من المعلومات المقدّمة شفهيًا عندما تم اختبارهم بعد 72 ساعة من التعليم، ولكن المفاجأة أن زادت النسبة إلى 65% تقريبًا عند إضافة بعض الصور للمحتوى التعليمي.</p>
                    <p>يعود السبب وراء ذلك إلى<a href="https://en.wikipedia.org/wiki/Picture_superiority_effect">«تأثير التفوق البصري»</a>، حيث يقوم الدماغ البشري بمعالجة المعلومات بشكل أكثر فعالية عندما يتلقى المصطلحات التصويرية عوضًا عن المصطلحات النصية أو الشفوية، فقد لا يتذكر الطلاب بالضبط ما قاله المعلم للتوّ، ولكنهم سيتذكرون صورة أو فيديو ذا معنى قوي، فقد تحوي صورة واحدة في معناها ما يعادل ألف كلمة وكلمة.</p>
                    <h2>3. إدراك ما لا يمكن إدراكه من التعليم التقليدي</h2> <p>في الفصول
                        الدراسية العادية، نادرًا ما يكون هناك وقت لتعليم الطلاب كيفية قراءة الأبحاث
                        العلمية بشكل نقدي، وهذا ما يمكن إدراكه من خلال المتّسع الكبير من الوقت الذي
                        يحظى به الطالب في الفصل الدراسي الحالي.</p>
                    <p>فمن
                        خلال دورات التعليم الإلكترونية المختصة، يمكن تعليم الطلاب كيفية البحث في
                        المواقع والمجلات العلمية أمثال Nature وScience، وبالطبع قبل
                        قراءة الأوراق البحثية لابد وأن يطّلع على أحدث الأخبار والآراء للحصول على فهم
                        أفضل للاكتشافات العلمية الحديثة.</p>
                    <p>ستؤدي
                        المهارات البحثية التي يكتسبها الطلاب إلى زيادة دافع التعلّم لديهم بشكل كبير، بجانب
                        اكتسابهم الثقة لكونهم أكثر إنتاجيةً مما كانوا عليه في الفصول الدراسية التقليدية.</p>
                    <p>كما أنّ الفائدة ليست مقتصرة على الطلاب فحسب، فالتعليم الإلكتروني يسمح لأعضاء هيئة التدريس بالتجربة للوصول إلى الطريقة المثلى لاستخدام التكنولوجيا بشكل مباشر في عملية التعليم، وبغض النظر عن ماهية الدورة التدريبية، فالعمل على المستندات ودمج مواد الفيديو والصوت في محتوى الدورات التدريبية الخاصة بهم قد يساعد كثيرًا على تطوير مهاراتهم الحاسوبية، وستُترجم هذه المهارات الأساسية بدورها إلى العديد من المهن الأخرى في عصرنا الرقمي.</p>
                    <p>تعتبر فعالية التعليم عاملاً مُهمًا لمُصممي التعليم، فإنشاء تعليم إلكتروني فعال يمثّل تحديًا كبيرًا لدى المؤسسات التعليمية، ولكن يمكن التغلب عليه باستخدام منهاجية مناسبة كما ذكرنا في هذه المقالة.</p>
                    <p>يساعد التعليم الإلكتروني في تثبيط&nbsp;اضطراب العملية التعليمية الذي نواجهه في أيامنا الحالية إلى أقصى حدٍّ ممكن، ويسعى إلى تيسير استمرارية التعلّم، فنأمل أن يساعد هذا الانتقال المؤقت للعملية التعليمية على الحد من انتشار الفيروس، وأن يكون وسيلةً للحفاظ على العديد من أرواح الطلاب وأرواح من يحبون.</p>
